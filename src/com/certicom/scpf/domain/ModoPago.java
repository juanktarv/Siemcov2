package com.certicom.scpf.domain;

public class ModoPago {
	
	private Integer id_modo_pago;
	private String descripcion_modo_pago;
	private Integer numero_dias; 
	
	
	public Integer getId_modo_pago() {
		return id_modo_pago;
	}
	public void setId_modo_pago(Integer id_modo_pago) {
		this.id_modo_pago = id_modo_pago;
	}
	public String getDescripcion_modo_pago() {
		return descripcion_modo_pago;
	}
	public void setDescripcion_modo_pago(String descripcion_modo_pago) {
		this.descripcion_modo_pago = descripcion_modo_pago;
	}
	public Integer getNumero_dias() {
		return numero_dias;
	}
	public void setNumero_dias(Integer numero_dias) {
		this.numero_dias = numero_dias;
	}
	
	
	
	

}
